another_list = ['Hola', 3, 'mosqueteros', False, "Kiwi", "Pera"]

# Acceder a los elementos por rangos
# El rango selecciona desde el primer índice propuesto
# hasta el segundo indice propuesto pero excluyendo el
# segundo índice
print(another_list[2:5])
print(another_list[:4])
print(another_list[3:])
print(another_list[:])
print(another_list[-4:-1])
# Las listas se definen con []

first_list = ["Plátano", 'Kiwi', 'Manzana']

# Los elementos de una lista en python parten
# desde el cero

print(first_list[2])

# Por lo tanto, el último índice de una lista con n elementos sera: n - 1

# Hay una forma más sencilla de acceder al último elemento

print(first_list[-1])
print(first_list[-2])
print(first_list[-3])

